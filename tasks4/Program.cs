int n = int.Parse(Console.Readline());
if (n>0) {
    for (int i = 1; i <= n; i++) {
        if (i % 2 == 0) {
            Console.Writeline(i);
        }
    }
} else if (n < 0)
{
    for (int i = 1; i>= n; i--) {
        if (i % 2 == 0 && i=0) {
            Console.Writeline(i);
        }
    }
}
